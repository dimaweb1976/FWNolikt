CREATE DATABASE IF NOT EXISTS skladb
  CHARACTER SET utf8mb4
  COLLATE utf8mb4_unicode_ci;

CREATE USER IF NOT EXISTS 'fwnolikt'@'localhost' IDENTIFIED BY 'fwnolikt_demo';
CREATE USER IF NOT EXISTS 'fwnolikt'@'127.0.0.1' IDENTIFIED BY 'fwnolikt_demo';

GRANT ALL PRIVILEGES ON skladb.* TO 'fwnolikt'@'localhost';
GRANT ALL PRIVILEGES ON skladb.* TO 'fwnolikt'@'127.0.0.1';

USE skladb;

CREATE TABLE IF NOT EXISTS UNT_EXPORT (
  ID INT NOT NULL AUTO_INCREMENT,
  ARTICLE VARCHAR(64) NOT NULL,
  NAME VARCHAR(255) NULL,
  QTY DECIMAL(12, 3) NULL,
  PRICE DECIMAL(12, 2) NULL,
  CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (ID),
  UNIQUE KEY UX_UNT_EXPORT_ARTICLE (ARTICLE)
);

